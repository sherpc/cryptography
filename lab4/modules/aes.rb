  def self.sbox byte
    m = 0xf8
    r = 0
    q = GF.inv(byte) || 0

    for i in 0..7 do
      r = (r << 1) | (q & m).inject_bit { |r, b| r ^ b }
      m = (m >> 1) | ((m & 1) << 7)
    end

    r ^ 0x63
  end

  def self.inv_sbox byte
    #(0..255).find { |i| self.sbox(i) == byte }
    INV_SBOX[byte]
  end

  def self.rcon i
    [1.apply(i-1, &:mul_x), 0, 0, 0]
  end

  def self.key_expansion key
    w = []
    (0..Nk).each { |i| w[i] = key[4*i..(4 * i + 3)] }
    for i in Nk...(Nb * (Nr + 1)) do
      t = w[i-1]
      if i % Nk == 0
        sboxs = t.shift_left.map { |x| self.sbox x }
        t = sboxs.zip(self.rcon(i / Nk)).map { |x, y| x ^ y }
      end
      w[i] = w[i - Nk].zip(t).map { |x, y| x ^ y }
    end
    w
  end

  def self.state_from input
    s = [[], [], [], []]
    input.each_with_index { |x, i| s[i % 4][i / 4] = x }
    s
  end

  def self.output_from state
    output = []
    i = 0
    (0...Nb).each { |c| (0..3).each { |r| output[i] = state[r][c]; i += 1 } }
    output
  end

  def self.add_round_key s, w
    (0...Nb).each { |c| (0..3).each { |r| s[r][c] = s[r][c] ^ w[c][r] } }
    s
  end
  
  def self.apply_sbox s
    s.map_cell { |c| sbox c }
  end

  def self.apply_inv_sbox s
    s.map_cell { |c| inv_sbox c }
  end

  def self.shift_rows s
    r = []
    (0..3).each { |i| r[i] = s[i].shift_left(i) }
    r
  end

  def self.inv_shift_rows s
    r = []
    (0..3).each { |i| r[i] = s[i].shift_right(i) }
    r
  end

  def self.scalar_mul v1, v2
    v1.zip(v2).inject(0) { |s, x| s ^ GF.mul(x[0], x[1]) }
  end

  def self.mix_columns s, top_row
    for c in 0...Nb do
      col = []
      (0..3).each { |r| col[r] = s[r][c] }
      k = top_row
      for r in 0..3 do
        s[r][c] = scalar_mul(k, col)
        k = k.shift_right
      end
    end
    s
  end

  def self.cipher input, w
    s = state_from input
    s = add_round_key s, w[0...Nb]
    for i in 1...Nr do
      s = apply_sbox s
      s = shift_rows s
      s = mix_columns s, [2,3,1,1]
      s = add_round_key s, w[(Nb * i)...(Nb * (i+1))]
    end
    s = apply_sbox s
    s = shift_rows s
    s = add_round_key s, w[(Nb * Nr)...(Nb * (Nr+1))]
    output_from s    
  end

  def self.inv_cipher input, w
    s = state_from input
    s = add_round_key s, w[(Nb * Nr)...(Nb * (Nr+1))]
    (Nr-1).downto(1) do |i|
      s = inv_shift_rows s
      s = apply_inv_sbox s
      s = add_round_key s, w[(Nb * i)...(Nb * (i+1))]
      s = mix_columns s, [0x0e,0x0b,0x0d,0x09]
    end
    s = inv_shift_rows s
    s = apply_inv_sbox s
    s = add_round_key s, w[0...Nb]
    output_from s    
  end
end
